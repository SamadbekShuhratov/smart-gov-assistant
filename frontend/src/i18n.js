export function getUiText(language) {
  if (language && UI_TEXT[language]) {
    return UI_TEXT[language];
  }
  return UI_TEXT.uz;
}

export function getStoredLanguage() {
  try {
    const stored = localStorage.getItem("uiLanguage");
    if (stored && UI_TEXT[stored]) {
      return stored;
    }
  } catch {
    // TODO: add telemetry/logging for storage access failures
  }
  return "uz";
}

export function saveLanguage(language) {
  try {
    localStorage.setItem("uiLanguage", language);
  } catch {
    // TODO: add telemetry/logging for storage access failures
  }
}

export function translateServiceText(value, language, type = "text") {
  const text = String(value || "").trim();
  if (!text) {
    return "";
  }

  if (language === "uz") {
    return text;
  }

  const typeMap = SERVICE_TRANSLATIONS[type];
  if (!typeMap) {
    return text;
  }

  const langMap = typeMap[language] || {};
  const uzMap = typeMap.uz || {};

  return langMap[text] || uzMap[text] || text;
}
